function renderCategoryFilters(toolsData) {
  const categories = [...new Set(toolsData.map(tool => tool.category).filter(Boolean))].sort();
  categoryButtonsContainer.innerHTML = '';

  const allButton = document.createElement('button');
  allButton.classList.add('category-filter');
  allButton.textContent = 'Todas';
  allButton.dataset.filterType = 'category';
  allButton.dataset.filterValue = 'Todos';
  if (activeFilters.category === null) { allButton.classList.add('selected'); }
  categoryButtonsContainer.appendChild(allButton);

  categories.forEach(category => {
      const button = document.createElement('button');
      button.classList.add('category-filter');
      const icon = categoryIcons[category] || categoryIcons["default"];
      button.innerHTML = `<span aria-hidden="true">${icon}</span> ${category}`; // Adiciona ícone
      button.dataset.filterType = 'category';
      button.dataset.filterValue = category;
      if (activeFilters.category === category) { button.classList.add('selected'); }
      categoryButtonsContainer.appendChild(button);
  });
}

// --- Funções de Filtragem e Busca (applyAllFilters, updateFilterUI - sem mudanças da v2) ---
function applyAllFilters() {
  let filteredTools = [...tools];
  if (activeFilters.search) {
      const searchTerm = activeFilters.search.toLowerCase();
      filteredTools = filteredTools.filter(tool =>
          (tool.name || '').toLowerCase().includes(searchTerm) ||
          (tool.description || '').toLowerCase().includes(searchTerm) ||
          (tool.tags || []).some(tag => tag.toLowerCase().includes(searchTerm)) ||
          (tool.features || []).some(feature => feature.toLowerCase().includes(searchTerm)) ||
          (tool.category || '').toLowerCase().includes(searchTerm)
      );
  }
  if (activeFilters.category) { filteredTools = filteredTools.filter(tool => tool.category === activeFilters.category); }
  if (activeFilters.license) { filteredTools = filteredTools.filter(tool => tool.license === activeFilters.license); }
  if (activeFilters.language) { filteredTools = filteredTools.filter(tool => (tool.language || []).includes(activeFilters.language)); }
  filteredTools.sort((a, b) => (a.name || '').localeCompare(b.name || ''));
  renderTools(filteredTools);
}

function updateFilterUI(filterType, selectedValue) {
  // Atualiza tanto os botões de categoria quanto os itens de dropdown
  const elements = document.querySelectorAll(`[data-filter-type="${filterType}"]`);
  elements.forEach(el => {
      el.classList.toggle('selected', el.dataset.filterValue === selectedValue);
  });
}


// --- Funções de Favoritos (loadFavorites, saveFavorites, addFavorite, removeFavorite - sem mudanças da v2) ---
function loadFavorites() { const stored = localStorage.getItem('favoriteTools'); return stored ? JSON.parse(stored) : []; }
function saveFavorites() { localStorage.setItem('favoriteTools', JSON.stringify(favorites)); }
function addFavorite(toolId) { if (!favorites.includes(toolId)) { favorites.push(toolId); saveFavorites(); } }
function removeFavorite(toolId) { favorites = favorites.filter(id => id !== toolId); saveFavorites(); }

// --- Handlers de Eventos ---

/** Manipula cliques nos filtros de CATEGORIA (delegação na seção de tags) */
function handleCategoryFilterClick(event) {
  const target = event.target.closest('[data-filter-type="category"]');
  if (!target) return;
  handleFilterLogic(target); // Reusa a lógica de filtro
}

/** Manipula cliques nos filtros de DROPDOWN (delegação na toolbar do header) */
function handleDropdownFilterClick(event) {
  const target = event.target.closest('[data-filter-type="license"], [data-filter-type="language"]');
  if (!target) return;
  handleFilterLogic(target); // Reusa a lógica de filtro

  // Fecha o dropdown após seleção
  const dropdownContainer = target.closest('.filter-container');
  if (dropdownContainer && dropdownContainer.classList.contains('active')) {
      dropdownContainer.classList.remove('active');
      const toggle = dropdownContainer.querySelector('.filter-toggle');
      toggle.setAttribute('aria-expanded', 'false');
      dropdownContainer.querySelector('.filter-content').hidden = true;
  }
}

/** Lógica comum para processar um clique de filtro */
function handleFilterLogic(targetElement) {
  const filterType = targetElement.dataset.filterType;
  const filterValue = targetElement.dataset.filterValue;

  if (filterValue === 'Todos' || activeFilters[filterType] === filterValue) {
      activeFilters[filterType] = null;
  } else {
      activeFilters[filterType] = filterValue;
  }
  updateFilterUI(filterType, activeFilters[filterType]);
  applyAllFilters();
}


function handleSearchInput() { activeFilters.search = searchInput.value.trim(); applyAllFilters(); }
function handleSearchToggle() {
  const isActive = searchContainer.classList.toggle('active');
  searchButton.setAttribute('aria-expanded', String(isActive));
  searchInput.hidden = !isActive;
  if (isActive) { searchInput.focus(); }
  else { searchInput.value = ''; activeFilters.search = ''; applyAllFilters(); }
}
function handleFavoriteClick(event) {
  const button = event.currentTarget; const toolId = button.dataset.toolId; if (!toolId) return;
  const card = button.closest('.card'); const toolName = card?.querySelector('h2')?.textContent || 'esta ferramenta';
  const isFavorited = favorites.includes(toolId);
  if (isFavorited) { removeFavorite(toolId); } else { addFavorite(toolId); }
  button.classList.toggle('favorited', !isFavorited);
  button.setAttribute('aria-pressed', String(!isFavorited));
  button.setAttribute('aria-label', `${!isFavorited ? 'Remover dos favoritos:' : 'Adicionar aos favoritos:'} ${toolName}`);
  button.setAttribute('title', !isFavorited ? 'Remover dos favoritos' : 'Adicionar aos favoritos');
}
function handleDropdownToggle(event) {
  event.stopPropagation(); const toggle = event.currentTarget; const container = toggle.closest(".filter-container"); const content = container.querySelector(".filter-content"); const isActive = container.classList.toggle("active");
  toggle.setAttribute('aria-expanded', String(isActive)); content.hidden = !isActive;
  document.querySelectorAll(".filter-container.active").forEach(activeContainer => {
      if (activeContainer !== container) {
          activeContainer.classList.remove("active"); activeContainer.querySelector('.filter-toggle').setAttribute('aria-expanded', 'false'); activeContainer.querySelector('.filter-content').hidden = true;
      }
  });
}
function handleOutsideClick(event) {
  // Fecha dropdowns
  document.querySelectorAll(".filter-container.active").forEach(activeContainer => {
      if (!activeContainer.contains(event.target)) {
          activeContainer.classList.remove("active"); activeContainer.querySelector('.filter-toggle').setAttribute('aria-expanded', 'false'); activeContainer.querySelector('.filter-content').hidden = true;
      }
  });
  // Fecha busca se clicar fora dela E não for no botão de toggle
  if (searchContainer.classList.contains('active') && !searchContainer.contains(event.target) && event.target !== searchButton && !searchButton.contains(event.target)) {
     handleSearchToggle(); // Reusa a função de toggle para fechar e limpar
  }
}

/** Manipula o clique no botão de toggle das categorias */
function handleCategoryToggle() {
  const section = categoryTagFilterSection; // Usando a variável global
  const isActive = section.classList.toggle('active');
  toggleCategoriesButton.setAttribute('aria-expanded', String(isActive));
}


