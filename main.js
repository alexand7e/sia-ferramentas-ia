// --- Fun√ß√µes de Filtragem e Busca (applyAllFilters, updateFilterUI - sem mudan√ßas da v2) ---
function applyAllFilters() {
  let filteredTools = [...tools];
  if (activeFilters.search) {
      const searchTerm = activeFilters.search.toLowerCase();
      filteredTools = filteredTools.filter(tool =>
          (tool.name || '').toLowerCase().includes(searchTerm) ||
          (tool.description || '').toLowerCase().includes(searchTerm) ||
          (tool.tags || []).some(tag => tag.toLowerCase().includes(searchTerm)) ||
          (tool.features || []).some(feature => feature.toLowerCase().includes(searchTerm)) ||
          (tool.category || '').toLowerCase().includes(searchTerm)
      );
  }
  if (activeFilters.category) { filteredTools = filteredTools.filter(tool => tool.category === activeFilters.category); }
  if (activeFilters.license) { filteredTools = filteredTools.filter(tool => tool.license === activeFilters.license); }
  if (activeFilters.language) { filteredTools = filteredTools.filter(tool => (tool.language || []).includes(activeFilters.language)); }
  filteredTools.sort((a, b) => (a.name || '').localeCompare(b.name || ''));
  renderTools(filteredTools);
}

function updateFilterUI(filterType, selectedValue) {
  // Atualiza tanto os bot√µes de categoria quanto os itens de dropdown
  const elements = document.querySelectorAll(`[data-filter-type="${filterType}"]`);
  elements.forEach(el => {
      el.classList.toggle('selected', el.dataset.filterValue === selectedValue);
  });
}


// --- Fun√ß√µes de Favoritos (loadFavorites, saveFavorites, addFavorite, removeFavorite - sem mudan√ßas da v2) ---
function loadFavorites() { const stored = localStorage.getItem('favoriteTools'); return stored ? JSON.parse(stored) : []; }
function saveFavorites() { localStorage.setItem('favoriteTools', JSON.stringify(favorites)); }
function addFavorite(toolId) { if (!favorites.includes(toolId)) { favorites.push(toolId); saveFavorites(); } }
function removeFavorite(toolId) { favorites = favorites.filter(id => id !== toolId); saveFavorites(); }

