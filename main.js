// --- Funções de Renderização ---

function formatLicenseClass(license) {
  if (!license) return '';
  return license.toLowerCase().replace(/\s+|\(|\)/g, '-').replace(/-+/g, '-');
}

function renderUsability(level, toolName) {
  let dots = '';
  for (let i = 0; i < 5; i++) { dots += `<span class="dot ${i < level ? 'filled' : ''}"></span>`; }
  return `<div class="usability" aria-label="Avaliação de usabilidade para ${toolName}: ${level} de 5">${dots}<span class="tooltip-text">Usabilidade: ${level}/5</span></div>`;
}

function renderTools(toolsArray) {
  grid.innerHTML = '';
  if (toolsArray.length === 0) {
      grid.innerHTML = '<p style="grid-column: 1 / -1; text-align: center; color: #6c757d;">Nenhuma ferramenta encontrada.</p>';
      return;
  }

  toolsArray.forEach(tool => {
      const card = document.createElement('div');
      card.classList.add('card');
      card.dataset.toolId = tool.id;

      const isFavorited = favorites.includes(tool.id);
      const licenseClass = formatLicenseClass(tool.license);

      // **Correção Licença:** Só renderiza o span se tool.license tiver valor
      const licenseHTML = tool.license
          ? `<span class="license ${licenseClass}">${tool.license}</span>`
          : '';

      card.innerHTML = `
          ${licenseHTML}
          <div class="languages" ${!tool.language || tool.language.length === 0 ? 'style="display: none;"' : ''}>
              ${(tool.language || []).map(lang => `<span class="language">${lang}</span>`).join('')}
          </div>
          <div class="card-header">
              <img src="${tool.icon || 'placeholder.png'}" alt="" class="tool-icon" loading="lazy" onerror="this.style.display='none'"> <!-- Fallback/Error handling -->
              <div class="card-header-content">
                  <h2>${tool.name || 'Nome Indisponível'}</h2>
                  ${renderUsability(tool.usability || 0, tool.name || 'Ferramenta')}
              </div>
          </div>
          <p class="description">${tool.description || ''}</p>
          <ul class="features-list" ${!tool.features || tool.features.length === 0 ? 'style="display: none;"' : ''}>
              ${(tool.features || []).map(feature => `<li>${feature}</li>`).join('')}
          </ul>
          <div class="tags" ${!tool.tags || tool.tags.length === 0 ? 'style="display: none;"' : ''}>
              ${(tool.tags || []).map(tag => `<span class="tag">${tag}</span>`).join('')}
          </div>
          <a href="${tool.link || '#'}" class="link" target="_blank" rel="noopener noreferrer" ${!tool.link ? 'style="display: none;"' : ''}>
              Acessar Ferramenta <span class="sr-only">(abre em nova aba)</span>
          </a>
      `;
      const favButton = card.querySelector('.favorite-button');
      if (favButton) { favButton.addEventListener('click', handleFavoriteClick); }
      grid.appendChild(card);
  });
}

