/** Manipula cliques nos filtros de CATEGORIA (delega√ß√£o na se√ß√£o de tags) */
function handleCategoryFilterClick(event) {
  const target = event.target.closest('[data-filter-type="category"]');
  if (!target) return;
  handleFilterLogic(target); // Reusa a l√≥gica de filtro
}

/** Manipula cliques nos filtros de DROPDOWN (delega√ß√£o na toolbar do header) */
function handleDropdownFilterClick(event) {
  const target = event.target.closest('[data-filter-type="license"], [data-filter-type="language"]');
  if (!target) return;
  handleFilterLogic(target); // Reusa a l√≥gica de filtro

  // Fecha o dropdown ap√≥s sele√ß√£o
  const dropdownContainer = target.closest('.filter-container');
  if (dropdownContainer && dropdownContainer.classList.contains('active')) {
      dropdownContainer.classList.remove('active');
      const toggle = dropdownContainer.querySelector('.filter-toggle');
      toggle.setAttribute('aria-expanded', 'false');
      dropdownContainer.querySelector('.filter-content').hidden = true;
  }
}

/** L√≥gica comum para processar um clique de filtro */
function handleFilterLogic(targetElement) {
  const filterType = targetElement.dataset.filterType;
  const filterValue = targetElement.dataset.filterValue;

  if (filterValue === 'Todos' || activeFilters[filterType] === filterValue) {
      activeFilters[filterType] = null;
  } else {
      activeFilters[filterType] = filterValue;
  }
  updateFilterUI(filterType, activeFilters[filterType]);
  applyAllFilters();
}


function handleSearchInput() { activeFilters.search = searchInput.value.trim(); applyAllFilters(); }
function handleSearchToggle() {
  const isActive = searchContainer.classList.toggle('active');
  searchButton.setAttribute('aria-expanded', String(isActive));
  searchInput.hidden = !isActive;
  if (isActive) { searchInput.focus(); }
  else { searchInput.value = ''; activeFilters.search = ''; applyAllFilters(); }
}
function handleFavoriteClick(event) {
  const button = event.currentTarget; const toolId = button.dataset.toolId; if (!toolId) return;
  const card = button.closest('.card'); const toolName = card?.querySelector('h2')?.textContent || 'esta ferramenta';
  const isFavorited = favorites.includes(toolId);
  if (isFavorited) { removeFavorite(toolId); } else { addFavorite(toolId); }
  button.classList.toggle('favorited', !isFavorited);
  button.setAttribute('aria-pressed', String(!isFavorited));
  button.setAttribute('aria-label', `${!isFavorited ? 'Remover dos favoritos:' : 'Adicionar aos favoritos:'} ${toolName}`);
  button.setAttribute('title', !isFavorited ? 'Remover dos favoritos' : 'Adicionar aos favoritos');
}
function handleDropdownToggle(event) {
  event.stopPropagation(); const toggle = event.currentTarget; const container = toggle.closest(".filter-container"); const content = container.querySelector(".filter-content"); const isActive = container.classList.toggle("active");
  toggle.setAttribute('aria-expanded', String(isActive)); content.hidden = !isActive;
  document.querySelectorAll(".filter-container.active").forEach(activeContainer => {
      if (activeContainer !== container) {
          activeContainer.classList.remove("active"); activeContainer.querySelector('.filter-toggle').setAttribute('aria-expanded', 'false'); activeContainer.querySelector('.filter-content').hidden = true;
      }
  });
}
function handleOutsideClick(event) {
  // Fecha dropdowns
  document.querySelectorAll(".filter-container.active").forEach(activeContainer => {
      if (!activeContainer.contains(event.target)) {
          activeContainer.classList.remove("active"); activeContainer.querySelector('.filter-toggle').setAttribute('aria-expanded', 'false'); activeContainer.querySelector('.filter-content').hidden = true;
      }
  });
  // Fecha busca se clicar fora dela E n√£o for no bot√£o de toggle
  if (searchContainer.classList.contains('active') && !searchContainer.contains(event.target) && event.target !== searchButton && !searchButton.contains(event.target)) {
     handleSearchToggle(); // Reusa a fun√ß√£o de toggle para fechar e limpar
  }
}

